<h3>RESPONSABILIDADE SÓCIOAMBIENTAL – EMABI 2013</h3>
<br /><br />
<p><b>Gerenciamento de Resíduos</b></p>

<p>
O EMABI 2013, pela primeira vez, lança o compromisso com a responsabilidade sócioambiental, contando com o gerenciamento dos resíduos gerados durante todo o evento. Como meta principal está a correta separação dos resíduos gerados e seu encaminhamento para a Coopercicla, empresa localizada na cidade de Maringá e responsável pela destinação correta dos mesmos. 
</p>

<p><b>Recolhimento de pilhas e baterias</b></p>
<p>
As pilhas e baterias são compostas por metais maléficos à saúde do ser humano e nocivos ao meio ambiente. Entretanto, muitas pessoas ainda costumam joga-las no lixo comum, às vezes por não possuírem informações de como ou onde descarta-las corretamente. 
</p>

<p>
A comissão sócioambiental do Emabi 2013 estará recolhendo as pilhas e baterias que você possui em casa, e não sabe onde descarta-las, além de informar os locais, em Maringá, onde você poderá leva-la para descarte. Os pontos de coleta são responsáveis por darem um destino correto para esse tipo de resíduo.
</p>

