<p><b>ECOtenda</b></p>

<p>Uma Ecotenda, foi feita especialmente para aqueles estudantes que querem conhecer mais sobre planos de gerenciamento de resíduos, reciclagem, cooperativas de reciclagem, e também servirá de local para maiores esclarecimentos e detalhes sobre a organização sócioambiental do evento, além de informações sobre o apoio da Fundação Isis Bruder na sustentabilidade do Emabi 2013.</p>
<p>
Também teremos em um dos dias do evento, uma rápida Oficina para ensinar a fazer a lixeira sustentável, utilizada no Emabi 2013, aos que se interessarem pelo assunto.
</p>



