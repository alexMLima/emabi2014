com outros estudantes, trocando viv&ecirc;ncias e experi&ecirc;ncias, participar de palestras e
cursos de diversos temas e apresentar trabalhos e pesquisas desenvolvidos ao longo de
sua vida acad&ecirc;mica.</p>

