<p>O curso de <strong>Ci&ecirc;ncias Biol&oacute;gicas na Universidade Estadual de Maring&aacute;</strong> foi
implantado no 1&ordm; semestre de 1973 e reconhecido atrav&eacute;s do Decreto n&ordm; 77.584-MEC,
de 11 de maio de 1976, completando este ano portanto, 40 anos de curso, com mais de
2000 mil profissionais formados ao longo desses anos.</p>

<p>A <strong>Universidade Estadual de Maring&aacute;</strong> foi eleita a melhor universidade do estado
pelo MEC em 2012; o curso de Licenciatura em Ci&ecirc;ncias Biol&oacute;gicas alcan&ccedil;ou nota 4 no
ENADE em 2012 e nota 3 no Bacharel, tendo os dois cursos nota 4 em infra estrutura,
sendo portanto, motivo de grande orgulho para a Universidade.</p>

<p>A Semana da Biologia tem sido uma tradi&ccedil;&atilde;o do curso, atraindo universit&aacute;rios de
